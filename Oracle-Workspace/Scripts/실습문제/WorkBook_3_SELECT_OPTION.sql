-- 18.
SELECT ts.STUDENT_NO, ts.STUDENT_NAME
FROM TB_STUDENT ts 
JOIN TB_GRADE tg ON (ts.STUDENT_NO = tg.STUDENT_NO)
JOIN TB_DEPARTMENT td ON (ts.DEPARTMENT_NO = td.DEPARTMENT_NO)
WHERE td.DEPARTMENT_NAME = '국어국문학과'
GROUP BY ts.STUDENT_NO, ts.STUDENT_NAME
HAVING AVG(POINT) = (
	SELECT MAX(AVG(POINT))
	FROM TB_GRADE tg2
	JOIN TB_STUDENT ts2 USING(STUDENT_NO)
	WHERE DEPARTMENT_NO = '001'
	GROUP BY STUDENT_NO
);

SELECT STUDENT_NO, STUDENT_NAME
FROM TB_STUDENT ts
JOIN TB_DEPARTMENT td USING(DEPARTMENT_NO)
JOIN TB_GRADE tg USING(STUDENT_NO)
WHERE td.DEPARTMENT_NAME = '국어국문학과'
GROUP BY STUDENT_NO, STUDENT_NAME
HAVING AVG(POINT) = (
	SELECT MAX(AVG(POINT))
	FROM TB_STUDENT ts
	JOIN TB_DEPARTMENT td USING(DEPARTMENT_NO)
	JOIN TB_GRADE tg USING(STUDENT_NO)
	WHERE td.DEPARTMENT_NAME = '국어국문학과'
	GROUP BY STUDENT_NO, STUDENT_NAME
);

-- INLINE VIEW 활용
SELECT STUDENT_NO, STUDENT_NAME
FROM (
	SELECT STUDENT_NO, STUDENT_NAME, AVG(POINT)
	FROM TB_GRADE tg 
	JOIN TB_STUDENT ts USING (STUDENT_NO)
	JOIN TB_DEPARTMENT td USING (DEPARTMENT_NO)
	WHERE DEPARTMENT_NAME = '국어국문학과'
	GROUP BY STUDENT_NO, STUDENT_NAME
	ORDER BY 3 DESC
)
WHERE ROWNUM = 1;

-- 19.
SELECT DEPARTMENT_NAME "계열 학과명", ROUND(AVG(POINT),1) "전공평점"
FROM TB_DEPARTMENT td
JOIN TB_CLASS tc USING (DEPARTMENT_NO)
JOIN TB_GRADE tg USING (CLASS_NO)
WHERE CATEGORY = (
	SELECT CATEGORY 
	FROM TB_DEPARTMENT
	WHERE DEPARTMENT_NAME = '환경조경학과'
)
AND CLASS_TYPE LIKE '전공%'
GROUP BY DEPARTMENT_NAME 
ORDER BY 1;






