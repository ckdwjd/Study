FROM TB_PROFESSOR tp
WHERE DEPARTMENT_NO = (
	SELECT DEPARTMENT_NO 
	FROM TB_DEPARTMENT td 
	WHERE DEPARTMENT_NAME = '법학과')
ORDER BY 2;

-- 5.
SELECT STUDENT_NO, TO_CHAR(POINT, 9.99)
FROM TB_GRADE tg 
WHERE CLASS_NO = 'C3118100' AND TERM_NO = '200402'
ORDER BY 2 DESC, STUDENT_NO;

