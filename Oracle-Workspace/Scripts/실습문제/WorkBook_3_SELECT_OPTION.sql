-- 12.
SELECT STUDENT_NAME, TERM_NO
FROM TB_STUDENT ts 
JOIN TB_GRADE tg USING (STUDENT_NO)
WHERE CLASS_NO = (
	SELECT CLASS_NO
	FROM TB_CLASS tc 
	JOIN TB_GRADE USING (CLASS_NO)
	WHERE CLASS_NAME = '인간관계론'
	GROUP BY CLASS_NO
) AND SUBSTR(TERM_NO,1,4) = '2007'
GROUP BY STUDENT_NAME, TERM_NO;

SELECT STUDENT_NAME, TERM_NO
