JOIN TB_GRADE tg USING (STUDENT_NO)
JOIN TB_CLASS USING (CLASS_NO)
WHERE CLASS_NAME = '인간관계론' 
AND TERM_NO LIKE '2007%';

-- 13.
SELECT CLASS_NAME, DEPARTMENT_NAME
FROM TB_CLASS tc 
LEFT JOIN TB_CLASS_PROFESSOR tcp USING (CLASS_NO)
LEFT JOIN TB_DEPARTMENT td USING (DEPARTMENT_NO)
WHERE PROFESSOR_NO IS NULL
AND CATEGORY = '예체능'
ORDER BY DEPARTMENT_NO , CLASS_TYPE DESC, CLASS_NAME;


-- 14.
SELECT e.STUDENT_NAME "학생이름",
	CASE WHEN e.COACH_PROFESSOR_NO IS NULL THEN '지도교수 미지정'
		 ELSE tp.PROFESSOR_NAME 
	END "지도교수"
FROM (
	SELECT STUDENT_NO, STUDENT_NAME, COACH_PROFESSOR_NO
	FROM TB_STUDENT ts 
	JOIN TB_DEPARTMENT td ON(ts.DEPARTMENT_NO = td.DEPARTMENT_NO)
	WHERE DEPARTMENT_NAME = '서반아어학과'
) e
LEFT JOIN TB_PROFESSOR tp ON (e.COACH_PROFESSOR_NO = tp.PROFESSOR_NO)
ORDER BY e.STUDENT_NO;


SELECT STUDENT_NAME, NVL(PROFESSOR_NAME, '지도교수 미지정')
FROM TB_STUDENT ts 
LEFT JOIN TB_PROFESSOR tp ON (COACH_PROFESSOR_NO = PROFESSOR_NO)
JOIN TB_DEPARTMENT td ON (ts.DEPARTMENT_NO = td.DEPARTMENT_NO)
WHERE DEPARTMENT_NAME = '서반아어학과'
ORDER BY STUDENT_NO;

-- 15.
SELECT STUDENT_NO "학번", STUDENT_NAME "학생이름", DEPARTMENT_NAME "학과이름", ROUND(AVG(POINT),1) "평점"
FROM TB_STUDENT ts 
JOIN TB_DEPARTMENT td USING (DEPARTMENT_NO)
JOIN TB_GRADE tg USING (STUDENT_NO)
WHERE ABSENCE_YN = 'N'
GROUP BY STUDENT_NO , STUDENT_NAME, DEPARTMENT_NAME
HAVING AVG(POINT) >= 4
ORDER BY 1;

-- 16.
SELECT CLASS_NO, CLASS_NAME, AVG(POINT)
FROM TB_CLASS tc 
JOIN TB_GRADE tg USING(CLASS_NO)
JOIN TB_DEPARTMENT td USING (DEPARTMENT_NO)
WHERE DEPARTMENT_NAME = '환경조경학과'
AND CLASS_TYPE LIKE '%전공%'
GROUP BY CLASS_NO , CLASS_NAME;

