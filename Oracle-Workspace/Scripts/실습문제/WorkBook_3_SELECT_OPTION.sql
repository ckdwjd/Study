-- 10.
SELECT STUDENT_NO, STUDENT_NAME, ROUND(AVG(POINT),1) 
FROM TB_GRADE tg 
JOIN TB_STUDENT ts USING (STUDENT_NO)
JOIN TB_DEPARTMENT td USING(DEPARTMENT_NO)
WHERE DEPARTMENT_NAME = '음악학과'
GROUP BY STUDENT_NO, STUDENT_NAME
ORDER BY STUDENT_NO;

-- 11.
SELECT DEPARTMENT_NAME "학과이름", "학생이름", "지도교수이름"
FROM (
	SELECT STUDENT_NAME "학생이름", PROFESSOR_NAME "지도교수이름" , ts.DEPARTMENT_NO
	FROM TB_STUDENT ts 
	JOIN TB_PROFESSOR tp ON (ts.COACH_PROFESSOR_NO = tp.PROFESSOR_NO)
	WHERE STUDENT_NO = 'A313047'
) e
JOIN TB_DEPARTMENT td ON (td.DEPARTMENT_NO = e.DEPARTMENT_NO)
WHERE DEPARTMENT_NAME = '경제학과'
GROUP BY DEPARTMENT_NAME, "학생이름", "지도교수이름";


