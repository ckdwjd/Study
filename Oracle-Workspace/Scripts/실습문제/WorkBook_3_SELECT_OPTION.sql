-- 2.
SELECT STUDENT_NAME, STUDENT_SSN
FROM TB_STUDENT ts 
WHERE ABSENCE_YN = 'Y'
ORDER BY 2 DESC;

-- 3.
SELECT STUDENT_NAME 학생이름, STUDENT_NO 학번, STUDENT_ADDRESS 거주지
FROM TB_STUDENT ts 
WHERE SUBSTR(STUDENT_ADDRESS, 1, 3) IN ('강원도','경기도')
AND NOT STUDENT_NO LIKE 'A%' 
ORDER BY 1;

-- 4.
