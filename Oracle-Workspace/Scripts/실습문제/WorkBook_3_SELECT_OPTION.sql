-- 9.
SELECT CLASS_NAME, PROFESSOR_NAME
FROM TB_CLASS tc
JOIN TB_CLASS_PROFESSOR tcp USING (CLASS_NO)
JOIN TB_PROFESSOR tp USING(PROFESSOR_NO)
